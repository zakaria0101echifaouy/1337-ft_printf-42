#include "ft_printf.h"

void	ft_putnbr(int	n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	else if (n < 10)
	{
		ft_putchar('0' + n);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}